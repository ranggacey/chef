// Function to translate ingredient name based on current language
export function translateIngredient(name: string, targetLanguage: 'en' | 'id'): string {
  // Convert to lowercase for case-insensitive matching
  const lowerName = name.toLowerCase();
  
  // First, try to find an exact match
  for (const [key, translation] of Object.entries(ingredientTranslations)) {
    if (key.toLowerCase() === lowerName || 
        translation.en.toLowerCase() === lowerName || 
        translation.id.toLowerCase() === lowerName) {
      return translation[targetLanguage];
    }
  }
  
  // If no exact match, try to find a partial match
  for (const [key, translation] of Object.entries(ingredientTranslations)) {
    if (lowerName.includes(key.toLowerCase()) || 
        lowerName.includes(translation.en.toLowerCase()) || 
        lowerName.includes(translation.id.toLowerCase())) {
      // If we find a partial match, return the translation
      return translation[targetLanguage];
    }
  }
  
  // If no match is found, return the original name
  return name;
}

// Function to translate category based on current language
export function translateCategory(category: string, targetLanguage: 'en' | 'id'): string {
  const categoryKey = category.toLowerCase();
  
  if (categoryTranslations[categoryKey]) {
    return categoryTranslations[categoryKey][targetLanguage];
  }
  
  return category;
}

